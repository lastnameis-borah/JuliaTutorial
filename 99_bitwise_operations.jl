# Bitwise operators
println("Bitwise AND of 5 and 3 is: ", 5 & 3)
println("Bitwise OR of 5 and 3 is: ", 5 | 3)
println("Bitwise NOT of 5 is: ", ~5)
println("Shifting bits of 6 by one place to the right, we get: ", 6 >> 1)
println("Shifting bits of 2 by one place to the left, we get: ", 2 << 1)
println()